public class Cake {
    String flour;
    String sauce;
    String icing;
    String sugar;

    Cake(String flour, String sauce, String icing, String sugar){
        this.flour = flour;
        this.sauce = sauce;
        this.sugar = sugar;
        this.icing = icing;
    }

    Cake(String flour, String sauce, String sugar){
        this.flour = flour;
        this.sauce = sauce;
        this.sugar = sugar;
    }
}
